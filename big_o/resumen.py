# Este es el mapa mental que usamos para clasificar algoritmos:
#
#   O(1)        → Constante
#                 Siempre tarda lo mismo, sin importar el tamaño de n.
#
#   O(log n)    → Logarítmica
#                 Reduce el problema a la mitad en cada paso.
#                 Ejemplo: búsqueda binaria.
#
#   O(n)        → Lineal
#                 Recorre la entrada una vez.
#
#   O(n log n)  → Intermedia muy común
#                 Suele aparecer en algoritmos de ordenamiento eficientes.
#
#   O(n²)       → Cuadrática
#                 Dos bucles anidados. Crece muy mal.
#
#   O(2ⁿ)       → Exponencial
#                 Cada paso duplica el trabajo. Se vuelve inviable rápido.
#
#   O(n!)       → Factorial
#                 El infierno. Imposible de manejar incluso con n pequeños.
#
# Conclusión:
#   Las complejidades determinan la escalabilidad real de un algoritmo.
#   Saber identificarlas ayuda a evitar cuellos de botella devastadores.
